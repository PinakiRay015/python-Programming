# This statement will give 'true' as both the conditions are satisfying here
print(10 > 1 and 4 > 2)

# This statement will give 'false' as both the conditions not satisfying here
print(10 > 1 and 4 < 2)

# This statement will give 'true' as any one condition is satisfying here
print(10 > 1 or 4 < 2)
